#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	val;
	size_t			i;

	if (!s)
		return (NULL);
	ptr = (unsigned char *)s;
	val = (unsigned char)c;
	i = 0;
	while (i < n)
		ptr[i++] = val;
	return (s);
}
