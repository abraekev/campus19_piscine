int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	if (*s1 == '\0' && *s2 == '\0')
		return (0);
	while ((s1[i] != '\0' || s2[i] != '\0') && i < n)
	{
		if (s1[i] - s2[i] != 0)
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
